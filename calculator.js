// adds two numbers
function add(num1, num2) {
    console.log(num1 + num2);
    
}

// multiplies two numbers
function mul(num1, num2) {
    console.log(num1 * num2);
    
}

// subtracts two numbers
function sub(num1, num2) {
    console.log(num1 - num2);
    
}

// divides two numbers
function div(num1, num2) {
    console.log(num1 / num2);
    
}


