// adds two numbers
function add(num1, num2) {
    return num1 + num2;
    
}

// multiplies two numbers
function mul(num1, num2) {
    return num1 * num2;
    
}

// subtracts two numbers
function sub(num1, num2) {
    return num1 - num2;
    
}

// divides two numbers
function div(num1, num2) {
    return num1 / num2;
    
}

console.log(add(1, 1))
console.log(mul(2, 2))
console.log(sub(4, 2))
console.log(div(4, 2))